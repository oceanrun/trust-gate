import rateLimit from 'express-rate-limit'

export const debugLimiter = rateLimit({
  windowMs: 60_000,
  max: 10,
  standardHeaders: true,
  legacyHeaders: false,
  message: { error: 'Too many requests — limit 10/min for debug endpoint' },
})

